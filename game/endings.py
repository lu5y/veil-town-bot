class Endings:
    @staticmethod
    def quiet_survival():
        return (
            "The town remains.\n"
            "Too many questions are left unanswered.\n"
            "No one celebrates."
        )

    @staticmethod
    def collapse():
        return (
            "The town cannot continue.\n"
            "Too many are gone.\n"
            "No agreement is reached."
        )

    @staticmethod
    def moral_failure():
        return (
            "The threat is gone.\n"
            "The cost remains.\n"
            "No one feels clean."
        )

    @staticmethod
    def cycle():
        return (
            "You survived.\n"
            "The town prepares again.\n"
            "Nothing was learned."
        )
